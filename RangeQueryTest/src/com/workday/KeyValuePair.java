package com.workday;

public class KeyValuePair {
  private long key;
  private long value;

  private long getKey() {
    return key;
  }

  private void setKey(long key) {
    this.key = key;
  }

  private long getValue() {
    return value;
  }

  private void setValue(long value) {
    this.value = value;
  }

}
